#Aula de métodos ou funções do Curso "Ruby para Iniciantes", Aula 10
def soma(x,y)
  x + y
end

def subt(x,y)
  x - y
end

def mult(x,y)
  x* y
end

def div(x, y)
  x/y
end     

puts soma 2, 4 #Os parâmetros ou argumentos podem ser passados com parênteses ou não.
puts subt (5, 6)
puts mult 3, 9
puts div 10, 5